/**
 * Интерфейс для отображения содержимого справки.
 * Определяет стандартный метод display() для всех типов контента.
 */
public interface ContentDisplay {

    /**
     * Отображает содержимое справки.
     * Каждый класс, реализующий этот интерфейс, должен предоставить
     * свою реализацию отображения контента.
     */
    void display();
}
